package kz.past.helpers.bean;

public class MasterPriceBean {
	int id;
	int idMaster;
	int idProc;
	int idProcType;
	int price;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getIdMaster() {
		return idMaster;
	}
	public void setIdMaster(int idMaster) {
		this.idMaster = idMaster;
	}
	public int getIdProc() {
		return idProc;
	}
	public void setIdProc(int idProc) {
		this.idProc = idProc;
	}
	public int getIdProcType() {
		return idProcType;
	}
	public void setIdProcType(int idProcType) {
		this.idProcType = idProcType;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
}
